// True Value Statement Examples
var result1 = 10 > 5;
console.log(result1, typeof result1); 

var result2 = (5 + 2) === 7;
console.log(result2, typeof result2); 

var result3 = "Hello".length > 0;
console.log(result3, typeof result3); 


// False Value Statement Examples
